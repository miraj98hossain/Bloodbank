		 	<h4 class="col s12 m12">FAQs</h4>
			 <ul class="col s12 m12 collapsible popout" data-collapsible="accordion">
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>How much blood is taken?</div>
				  <div class="collapsible-body">
				  <p>For a whole blood donation, approximately 0.5 L of blood is collected. 
For donations of other blood products, such as platelet or plasma, the amount collected depends on your height,
weight and platelet count.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>Are the history questions needed every time I donate?</div>
				  <div class="collapsible-body">
				  <p>Yes, since this helps to ensure the safest possible blood supply. All donors must be asked all the screening questions at each donation.
Both AABB and FDA regulations specifically require that all blood donors complete the donor history questionnaire on the day of donation and prior to donating.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>If I have a cood or a flu,can I donate blood?</div>
				  <div class="collapsible-body">
				  <p>No, blood centers require that you be in good health (symptom-free) and feeling well.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>Can I still donate if I have high blood pressure?</div>
				  <div class="collapsible-body">
				  <p>Yes, if your blood pressure falls within the limits set by FDA regulations.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>How long will the actual donation process take?</div>
				  <div class="collapsible-body">
				  <p>The entire donation process, from registration to post-donation refreshments, takes about one hour.The actual donation takes about 5-10 minutes.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>Who needs blood?</div>
				  <div class="collapsible-body">
				  <p>The need for blood is great. Every day in the U.S., approximately 29,000 units of red blood cells are required in hospitals and emergency treatment 
for patients with cancer and other diseases, for organ transplant recipients, and to help save the lives of accident/trauma victims. In addition,
nearly 5,000 platelet units and 6,500 units of plasma are also needed. In 2017, nearly 16 million blood components were transfused. With an aging
population and advances in medical treatments and procedures requiring blood transfusions, there is always a need for blood and blood components.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>What fees are associated with blood?</div>
				  <div class="collapsible-body">
				  <p>While donated blood is free, there are significant costs associated with collecting, testing, preparing components, labeling, storing and shipping blood; recruiting
and educating donors; and quality assurance. As a result, processing fees are charged to recover costs. Processing fees for individual blood components vary 
considerably. Processing fees for one specific component also may vary in different geographic regions. Hospitals charge for any additional testing that may be required,
such as the crossmatch, as well as for the administration of the blood.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>What is the 'availability of blood'?</div>
				  <div class="collapsible-body">
				  <p>Blood availability refers to the overall supply nationally or regionally. The blood supply varies among the many regions throughout the U.S. The blood supply
level also fluctuates throughout the year. Factors impacting blood inventories often include holidays/travel schedules, inclement weather and illness, such
as the outbreak of COVID-19.Historically, blood collections can be slow during the winter and summer months. A reduction in donor turnout rapidly impacts our nation's
blood inventory.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>Can I donate blood?</div>
				  <div class="collapsible-body">
				  <p>To donate blood, you must weigh at least 110 pounds (about 50 kilograms) and be at least 16 or 17 years old, depending on the law in your state. Some states allow 
legal minors to donate with parental permission. You must be in good health and able to pass the physical and a confidential health-history assessment to help make
sure blood donation is safe for both you and the recipient of the blood.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>What types of tests are performed on donated blood?</div>
				  <div class="collapsible-body">
				  <p>After blood is drawn, it is tested for ABO group (blood type) and Rh type (positive or negative), as well as for any unexpected red blood cell antibodies that may 
cause problems for the transfusion recipient.
Blood is tested for:<br />
				  <ul>
				  	<li>Hepatitis B virus</li>
				  	<li>Hepatitis C virus</li>
				  	<li>HIV-1 and HIV-2</li>
				  	<li>HTLV-I and HTLV-II</li>
				  	<li>Syphilis</li>
				  	<li>West Nile virus</li>
				  	<li>Trypanosoma cruzi, the infectious agent causing Chagas' disease</li>
				  	<li>Zika virus</li>
				  	<li>Babesia – in states where testing is required by FDA guidance</li>
				  </ul>
				  </p>
				 </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>What is the most common blood type?</div>
				  <div class="collapsible-body">
				  <p>The approximate distribution of blood types in the U.S. blood donor population is as follows. Distribution may be different for specific racial
and ethnic groups and in different parts of the country:
				  <ul>
				  	<li>•	O Rh-positive --- 39 percent</li>
				  	<li>•	O Rh-negative ---  9 percent</li>
				  	<li>•	A Rh-positive --- 30 percent</li>
				  	<li>•	A Rh-negative ---  6 percent</li>
				  	<li>•	B Rh-positive ---    9 percent</li>
					<li>•	B Rh-negative ---   2 percent</li>
					<li>•	AB Rh-positive ---  4 percent</li>
					<li>•	AB Rh-negative --- 1 percent</li>
					  </ul>
				  </p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>Can I donate blood if I have recently had a vaccination?</div>
				  <div class="collapsible-body">
				  <p>You may donate blood after most vaccinations if you're feeling well. You'll have to wait to donate for two to four weeks after being vaccinated for chickenpox, measles,
mumps,rubella or smallpox, or receiving the oral polio vaccine.
If you've had a COVID-19 vaccination, discuss your eligibility for donating blood with center staff.
</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>How can I prepare to donate blood?</div>
				  <div class="collapsible-body">
				  <p>Before donating, eat a healthy meal and drink plenty of water. Go to your donation appointment well rested and wearing a shirt with sleeves that can be rolled up above
your elbows. Bring your donor card, driver's license or two other forms of ID.</p>
				   </div>
				</li>
				<li>
				  <div class="collapsible-header"><i class="material-icons">picture_in_picture</i>Are 16 years olds allow to donate blood?</div>
				  <div class="collapsible-body">
				  <p>Nearly 30 states (including Illinois, Indiana, Michigan, Ohio and Wisconsin) permit 16-year-olds to donate blood with consent from a parent/guardian. These donors must
be in good health and meet the minimum criteria to donate. Please refer to the height and weight chart to verify donor eligibility. By becoming a blood donor, your son
or daughter shows great civic responsibility, maturity and community pride.</p>
				   </div>
				</li>
				
